# Python Comparison Operators

num1 = 10
num2 = 20

print("Num1 = ", num1)
print("Num2 = ", num2)
#Equal to operator(==)
print("num1 == num2 ", num1==num2)

# Not Equal to  Operator (!=)
print("num1 != num2 ", num1!=num2)

# Greater than Operator ( >)
print("num1 > num2 ", num1>num2)

# Less than Operator (<)
print("num1 < num2 ", num1<num2)

# greater Than Equal to Operator (>=)
print("num1 >= num2 ", num1>=num2)

# Less Than Equla to Operator (<=)
print("num1 <= num2 ", num1<=num2)